<script>
    let textoCompleto = '"BIENVENIDO"';
    let indice = 0;
    let elemento = document.getElementById('textoAnimado');

    function mostrarLetraPorLetra() {
        if (indice < textoCompleto.length) {
            elemento.textContent += textoCompleto[indice];
            indice++;
            setTimeout(mostrarLetraPorLetra, 100);  // Aquí 100 es el tiempo en milisegundos entre cada letra
        } else {
            setTimeout(() => {
                elemento.textContent = ""; // Borra el texto después de mostrarlo completamente
                indice = 0; // Resetea el índice
                mostrarLetraPorLetra(); // Comienza de nuevo
            }, 5000 - (textoCompleto.length * 100));  // Espera el tiempo restante antes de repetir (20 segundos en total)
        }
    }

    // Comienza el proceso la primera vez
    mostrarLetraPorLetra();

</script>
